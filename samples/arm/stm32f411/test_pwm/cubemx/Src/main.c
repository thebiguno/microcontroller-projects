int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();

  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim2,TIM_CHANNEL_1);
  
#define MPU_6050_ADDRESS (0x68 << 1)
  
  uint8_t data[32];
  data[0] = 0x6B;
  data[1] = 0x80;
  HAL_I2C_Master_Transmit(&hi2c1, MPU_6050_ADDRESS, data, 2, 1000);	//Reset
  HAL_Delay(100);
  data[1] = 0x00;
  HAL_I2C_Master_Transmit(&hi2c1, MPU_6050_ADDRESS, data, 2, 1000);	//Wake up
  HAL_Delay(500);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	for (uint16_t i = 0; i < htim2.Init.Period; i++){
		__HAL_TIM_SetCompare(&htim2, TIM_CHANNEL_1, i);
		HAL_Delay(2);
	}
	for (uint16_t i = htim2.Init.Period; i > 0; i--){
		__HAL_TIM_SetCompare(&htim2, TIM_CHANNEL_1, i);
		HAL_Delay(2);
	}
	
	data[0] = 0x3B;
	HAL_I2C_Master_Transmit(&hi2c1, MPU_6050_ADDRESS, data, 1, 1000);	//Set address to ACCEL_X_H
	HAL_I2C_Master_Receive(&hi2c1, MPU_6050_ADDRESS, data, 6, 1000);	//Read 6 bytes (accel data)
	char buffer[32];
	uint8_t len = snprintf(buffer, sizeof(buffer), "Data: %X %X %X %X %X %X\n", data[0], data[1], data[2], data[3], data[4], data[5]);
	HAL_UART_Transmit(&huart2, (uint8_t *) buffer, len, 0xFFFF); 
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;

  __PWR_CLK_ENABLE();

  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);

  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.HSICalibrationValue = 16;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSI;
  RCC_OscInitStruct.PLL.PLLM = 8;
  RCC_OscInitStruct.PLL.PLLN = 100;
  RCC_OscInitStruct.PLL.PLLP = RCC_PLLP_DIV2;
  RCC_OscInitStruct.PLL.PLLQ = 4;
  HAL_RCC_OscConfig(&RCC_OscInitStruct);

  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV4;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV4;
  HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_2);

  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

